"use client";

import {
    Dialog,
    DialogContent,
    DialogDescription,
    DialogFooter,
    DialogHeader,
    DialogTitle
} from "@/components/ui/dialog";
import z from "zod";
import {
    Form,
    FormControl,
    FormDescription,
    FormField,
    FormItem,
    FormLabel,
    FormMessage
} from "@/components/ui/form";
import { Textarea } from "@/components/ui/textarea";
import { zodResolver } from "@hookform/resolvers/zod";
import {useForm} from "react-hook-form";
import {  useEffect } from "react";
import { Input } from "@/components/ui/input";
import { Button } from "@/components/ui/button";


const formSchema = z.object({
    variableName: z
    .string()
    .min(1, {message: "variable name is required"})
    .regex(/^[A-Za-z_$][A-Za-z0-9_$]*$/, {
        message: "Variable name is must be start with a letter or underscore and container only letters, numbers, and underscores",
    }),
    username: z.string().optional(),
    content: z
        .string()
        .min(1, "Message content is required")
        .max(2000, "Discord messages cannot exceed 2000 characters"),
    webhookUrl: z.string().min(1,"Webhook URL is required"),

});

export type DiscordFormValues =z.infer<typeof formSchema>;

interface Props {
    open: boolean;
    onOpenChange: (open: boolean) => void;
    onSubmit: (values: z.infer<typeof formSchema>) => void;
    defaultValues?: Partial<DiscordFormValues>;
};  

export const DiscordDialog = ({
    open,
    onOpenChange,
    onSubmit,
    defaultValues = {},
}: Props) => {
    const form = useForm<z.infer<typeof formSchema>>({
        resolver: zodResolver(formSchema),
        defaultValues: {
            variableName: defaultValues.variableName || "", 
            username: defaultValues.username || "",
            content: defaultValues.content || "",
            webhookUrl: defaultValues.webhookUrl || "",
        },
    });

    // Reset form values when dialog opens with new defualts

    useEffect(() => {
        if (open) {
            form.reset({
            variableName: defaultValues.variableName || "", 
            username: defaultValues.username || "",
            content: defaultValues.content || "",
            webhookUrl: defaultValues.webhookUrl || "",
            })
        }
    }, [open, defaultValues, form])

     const watchVariablename = form.watch("variableName") || "myDiscord";
     

     const handleSubmit = (values: z.infer<typeof formSchema>) =>{
        onSubmit(values);
        onOpenChange(false);
     }

    return (
        <Dialog open={open} onOpenChange={onOpenChange}>
            <DialogContent className="max-h-[80vh] overflow-y-auto">
                <DialogHeader>
                    <DialogTitle>Discord Configuration</DialogTitle>
                    <DialogDescription>
                        Configure the Discord webhook settings for this node.
                    </DialogDescription>
                </DialogHeader>
                <Form {...form}>
                    <form
                        onSubmit={form.handleSubmit(handleSubmit)}
                        className="space-y-8 mt-4"
                    > 
                    <FormField
                            control={form.control}
                            name="variableName"
                            render={({ field }) => (
                            <FormItem>
                                <FormLabel>variable Name</FormLabel>
                                    <FormControl>
                                        <Input
                                            placeholder="myDiscord"
                                            {...field}
                                        />
                                    </FormControl>
                                <FormDescription>
                                    Use this name to refrence the result in other nodes: {" "}
                                    {`{{${watchVariablename}.text}}}`}
                                </FormDescription>
                                <FormMessage/>
                            </FormItem>
                            )}
                    />  
                        <FormField
                                control={form.control}
                                name="webhookUrl"
                                render={({ field}) => (
                                    <FormItem>
                                        <FormLabel>Webhook URL</FormLabel>
                                            <FormControl>
                                                <Input
                                                    placeholder="https://discord.com/api/webhooks/..."
                                                    {...field}
                                                />
                                            </FormControl>
                                            <FormDescription>
                                                 Get this from Discord: Channel Settings → Integrations → Webhooks
                                            </FormDescription>
                                        <FormMessage/>
                                    </FormItem>
                                )}
                        />
                        <FormField
                                control={form.control}
                                name="content"
                                render={({ field }) => (
                                <FormItem>
                                    <FormLabel>Message Content</FormLabel>
                                        <FormControl>
                                            <Textarea
                                                placeholder="Summary: {{myGemini.text}}"       
                                                className="min-h-[80px] font-mono text-sm"
                                                {...field}
                                            />
                                        </FormControl>
                                        <FormDescription>
                                            The message to send. Use {"{{variables}}"} for simple values or {"{{json variable}}"} to stringify objects
                                        </FormDescription>

                                    <FormMessage/>
                                </FormItem>
                                )}
                        />

                        <FormField
                            control={form.control}
                            name="username"
                            render={({ field}) => (
                                <FormItem>
                                    <FormLabel>Bot Username (Optional)</FormLabel>
                                        <FormControl>
                                            <Input
                                                placeholder="Workflow Bot"
                                                {...field}
                                            />
                                        </FormControl>
                                        <FormDescription>
                                            Ovveride the webhook's defualt username
                                        </FormDescription>
                                        <FormMessage/>
                                </FormItem>
                                )}
                        />
                        
                        
                        <DialogFooter className="mt-4">
                            <Button type="submit">Save</Button>
                        </DialogFooter>
                    </form> 
                </Form>
            </DialogContent>
        </Dialog>
    )
}
